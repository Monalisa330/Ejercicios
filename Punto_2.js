//Calcule el cuadrado de 2 números ingresados.

let a = parseInt(prompt("Ingresa el primer número:"));
let b = parseInt(prompt("Ingresa el segundo número:"));

let cuadradoA = a * a;
let cuadradoB = b * b;

console.log("El cuadrado del primer número (" + a + ") es: " + cuadradoA);
console.log("El cuadrado del segundo número (" + b + ") es: " + cuadradoB);
